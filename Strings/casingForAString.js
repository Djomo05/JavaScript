/**
 * To transform a string to all uppercase characters we can use the .toUpperCase() method.
 * To transform a string to all lowercase characters we can use the .toLowerCase() method.
 *
 * This approach is very useful when we want to have some consistency in our data or also
 * during comparisons.
 *
 * **/

let greeting = "Hello, World!";
let uppercaseGreeting = greeting.toUpperCase();
console.log(uppercaseGreeting);  // "HELLO, WORLD!"


let shout = "I AM LEARNING JAVASCRIPT!";
let lowercaseShout = shout.toLowerCase();
console.log(lowercaseShout);  // "i am learning javascript!"
