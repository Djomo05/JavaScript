/**
 * A Compound Assignment Operator is used when in one line of code we evaluate an expression and
 * re-assign the result back to the variable
 *
 * **/

// Before
let num = 5;
num = num + 2;

console.log(num); // 7

// After
num = 5;
num += 2;

console.log(num); // 7

let total = 10;
total += 5;

console.log(total); // 15


let score = 20;
score -= 7;

console.log(score); // 13


let points = 5;
points *= 3;

console.log(points); // 15


let balance = 100;
balance /= 4;

console.log(balance); // 25

